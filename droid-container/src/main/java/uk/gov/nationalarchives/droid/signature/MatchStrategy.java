package uk.gov.nationalarchives.droid.signature;

import java.io.IOException;

import uk.gov.nationalarchives.droid.core.interfaces.IdentificationRequest;
import uk.gov.nationalarchives.droid.core.interfaces.IdentificationResultCollection;

/**
 * A simple interface for different signature matching strategies.
 * This makes it possible to use different match strategies and compare their performance and results.
 */
public interface MatchStrategy {

    /**
     * Returns a collection of results after matching a request using some matching strategy.
     *
     * @param request The request to match.
     * @return A collection of results.
     * @throws IOException If there were any IO errors when reading data.
     */
    IdentificationResultCollection match(IdentificationRequest request) throws IOException;

}
